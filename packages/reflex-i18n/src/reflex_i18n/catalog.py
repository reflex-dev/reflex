"""Compile ``.po`` catalogs into the per-locale JS modules served to clients."""

from __future__ import annotations

import json
import re
from collections.abc import Sequence
from pathlib import Path
from typing import TYPE_CHECKING

from reflex_base.utils import console

from .config import LOCALE_COOKIE_NAME, I18nConfig
from .registry import MessageKey

if TYPE_CHECKING:
    from babel.messages.catalog import Catalog

# C plural expressions from the Plural-Forms header are (almost) valid JS;
# whitelist their tokens before embedding one in generated code.
_PLURAL_EXPR_ALLOWED = re.compile(r"^[ n0-9()%?:!<>=&|+*/-]+$")
_FALLBACK_PLURAL_EXPR = "n != 1"

_GENERATED_HEADER = "// Generated by Reflex. Do not edit.\n"


def read_po_catalog(path: Path) -> Catalog:
    """Parse a ``.po`` catalog file.

    Args:
        path: The path of the ``.po`` file.

    Returns:
        The parsed catalog.
    """
    from babel.messages.pofile import read_po

    with path.open("r", encoding="utf-8") as po_file:
        return read_po(po_file)


def _plural_expr_js(catalog: Catalog | None, locale: str) -> str:
    """Get the catalog's plural expression as a safe JS expression.

    Args:
        catalog: The parsed catalog, if one exists for the locale.
        locale: The locale being compiled, for warnings.

    Returns:
        The validated plural expression over the variable ``n``.
    """
    if catalog is None:
        return _FALLBACK_PLURAL_EXPR
    expr = catalog.plural_expr
    if not _PLURAL_EXPR_ALLOWED.match(expr):
        console.warn(
            f"Ignoring invalid Plural-Forms expression {expr!r} for locale "
            f"{locale!r}; falling back to {_FALLBACK_PLURAL_EXPR!r}."
        )
        return _FALLBACK_PLURAL_EXPR
    return expr


def compile_catalog_module(
    catalog: Catalog | None,
    used_messages: Sequence[MessageKey],
    locale: str,
    *,
    is_default_locale: bool,
) -> str:
    """Render the JS catalog module for one locale.

    Only messages actually used by the app are included (tree-shaking);
    untranslated messages are omitted so the client falls back to the msgid.

    Args:
        catalog: The parsed ``.po`` catalog, or None if the locale has none.
        used_messages: All messages collected from ``rx.t`` calls.
        locale: The locale being compiled, for warnings.
        is_default_locale: Whether this is the app's default locale, in which
            case missing translations are expected and not warned about.

    Returns:
        The JS module source code.
    """
    entries: list[str] = []
    missing: list[MessageKey] = []
    for key in used_messages:
        translation = _lookup_translation(catalog, key)
        if translation is None:
            missing.append(key)
            continue
        entries.append(f"  {json.dumps(key.catalog_key)}: {json.dumps(translation)},")
    if missing and not is_default_locale:
        missing_list = "\n".join(f"  {key.message!r}" for key in missing[:10])
        more = f"\n  ... and {len(missing) - 10} more" if len(missing) > 10 else ""
        console.warn(
            f"{len(missing)} translation(s) missing for locale {locale!r} "
            f"(falling back to the default locale):\n{missing_list}{more}"
        )
    messages_body = "\n".join(entries)
    return (
        f"{_GENERATED_HEADER}"
        f"export const plural = (n) => Number({_plural_expr_js(catalog, locale)});\n"
        f"export const messages = {{\n{messages_body}\n}};\n"
    )


def _lookup_translation(
    catalog: Catalog | None, key: MessageKey
) -> str | list[str] | None:
    """Find the translation for a message in a catalog.

    Args:
        catalog: The parsed catalog, or None.
        key: The message to look up.

    Returns:
        The translated string, a list of plural forms, or None if the message
        is untranslated (including partially translated plurals).
    """
    if catalog is None:
        return None
    message = catalog.get(key.msgid, key.context)
    if message is None:
        return None
    strings = message.string
    if key.plural is None:
        return strings or None if isinstance(strings, str) else None
    if isinstance(strings, str):
        strings = (strings,)
    if not strings or not all(strings):
        return None
    return list(strings)


def compile_index_module(config: I18nConfig) -> str:
    """Render the JS module describing the app's locales and catalog loaders.

    The static ``import()`` map lets the bundler code-split one chunk per
    locale, so clients only ever download the active language.

    Args:
        config: The app's i18n configuration.

    Returns:
        The JS module source code.
    """
    loaders = "\n".join(
        f"  {json.dumps(locale)}: () => import({json.dumps(f'$/i18n/{locale}.js')}),"
        for locale in config.locales
    )
    return (
        f"{_GENERATED_HEADER}"
        f"export const locales = {json.dumps(list(config.locales))};\n"
        f"export const defaultLocale = {json.dumps(config.default_locale)};\n"
        f"export const cookieName = {json.dumps(LOCALE_COOKIE_NAME)};\n"
        f"export const loaders = {{\n{loaders}\n}};\n"
    )
