EXPAND_THRESHOLD_LINES = 20
COLLAPSED_MAX_HEIGHT = "400px"


def _copy_button(code: str | rx.Var[str]) -> rx.Component:
    """Create the copy control used by static and client Shiki blocks.

    Args:
        code: Literal source copied by the button.

    Returns:
        The copy button.
    """
    copy_text = ShikiHighLevelCodeBlock._strip_transformer_triggers(code)
    return rx.el.button(
        rx.icon("copy", size=16, color=rx.color("gray", 11)),
        type="button",
        aria_label="Copy code",
        on_click=[
            rx.set_clipboard(copy_text),
            copy_script(),
        ],
        style={
            "position": "absolute",
            "top": "4px",
            "right": "4px",
            "background": rx.color("gray", 3),
            "border": "1px solid",
            "border_color": rx.color("gray", 5),
            "border_radius": "6px",
            "padding": "5px",
            "opacity": "1",
            "cursor": "pointer",
            "_hover": {"background": rx.color("gray", 4)},
            "transition": "background 0.12s ease-out",
            "&>svg": {"transition": "transform 0.12s ease-out, opacity 0.12s ease-out"},
            "_active": {"background": rx.color("gray", 5)},
        },
    )


def _static_shiki_block(
    code: str,
    html: str,
    *,
    can_copy: bool,
    show_line_numbers: bool,
    **props,
) -> rx.Component:
    """Render cached Shiki HTML in the same wrapper as the client component.

    Args:
        code: Literal source copied by the optional button.
        html: Highlighted HTML generated by Shiki.
        can_copy: Whether to render the copy button.
        show_line_numbers: Whether to apply line-number styles.
        **props: Wrapper component properties.

    Returns:
        A static highlighted code block.
    """
    copy_button = props.pop("copy_button", None)
    wrapper_style = props.pop("style", {})
    if show_line_numbers:
        wrapper_style = {**LINE_NUMBER_STYLING, **wrapper_style}
    class_name = props.pop("class_name", "")
    if isinstance(class_name, str):
        class_name = class_name.split()
    children = [rx.html(html, class_name="static-shiki-html")]
    if can_copy and copy_button is None:
        copy_button = _copy_button(code)
    if copy_button:
        children.append(copy_button)
        props["position"] = "relative"
    return rx.box(
        *children,
        class_name=" ".join(["static-shiki", *class_name]),
        style={**wrapper_style, **BOX_PARENT_STYLING},
        **props,
    )


def literal_shiki_code_block(
    code: str,
    *,
    client_component: Callable[..., rx.Component] = shiki_code_block,
    **props,
) -> rx.Component:
    """Render a literal code block through client or build-time Shiki.

    Args:
        code: Literal code to highlight.
        client_component: Original component used for development and discovery.
        **props: Shiki code block properties.

    Returns:
        The selected code block implementation.
    """
    if static_shiki.get_build_phase() == static_shiki.CLIENT_PHASE:
        return client_component(code, **props)

    if (
        props.get("decorations")
        or props.get("transformers")
        or props.get("use_transformers")
    ):
        msg = "Shiki decorations and transformers are not supported at build time."
        raise static_shiki.StaticShikiError(msg)

    language = props.get("language", "python")
    theme = props.get("theme")
    themes = props.get("themes")
    if themes is not None:
        normalized_themes = themes
    elif theme is None:
        normalized_themes = static_shiki.DEFAULT_THEMES
    elif isinstance(theme, str):
        mapped_theme = ShikiHighLevelCodeBlock._map_themes(theme)
        normalized_themes = {"light": mapped_theme, "dark": mapped_theme}
    else:
        msg = "Static Shiki requires literal theme names."
        raise static_shiki.StaticShikiError(msg)

    request = static_shiki.make_request(
        code,
        language=language,
        themes=normalized_themes,
    )
    html = static_shiki.discover_or_get_html(request)
    if html is None:
        return client_component(code, **props)

    static_props = dict(props)
    for key in (
        "decorations",
        "language",
        "theme",
        "themes",
        "transformers",
        "use_transformers",
        "code_tag_props",
    ):
        static_props.pop(key, None)
    return _static_shiki_block(
        code,
        html,
        can_copy=static_props.pop("can_copy", False),
        show_line_numbers=static_props.pop("show_line_numbers", False),
        **static_props,
    )


@rx.memo
def _plain_code_block(code: rx.Var[str], language: rx.Var[str]) -> rx.Component:
    """Shared plain code block implementation.

    Returns:
        The component.
    """
    return rx.box(
        shiki_code_block(
            code,
            language=language,
            class_name="code-block",
            can_copy=True,
        ),
        class_name="relative mb-4",
    )


def code_block(code: str, language: str):
    """Code block. Shows an Expand/Collapse toggle when longer than the threshold.

    Returns:
        The component.
    """
    # During import-graph introspection ``code`` may be a Var, not a Python str.
    # Skip the line-count check in that case and render the plain block.
    if not isinstance(code, str) or not isinstance(language, str):
        return _plain_code_block(code=code, language=language)
    if static_shiki.is_discovery():
        plain_block = literal_shiki_code_block(
            code,
            client_component=lambda *_args, **_kwargs: _plain_code_block(
                code=code, language=language
            ),
            language=language,
            class_name="code-block",
            can_copy=True,
        )
    elif static_shiki.is_render():
        plain_block = rx.box(
            literal_shiki_code_block(
                code,
                language=language,
                class_name="code-block",
                can_copy=True,
            ),
            class_name="relative mb-4",
        )
    else:
        plain_block = _plain_code_block(code=code, language=language)
    if code.count("\n") + 1 > EXPAND_THRESHOLD_LINES:
        return rx.el.div(
            plain_block,
            rx.el.details(
                rx.el.summary(
                    rx.el.span(
                        "Expand",
                        rx.icon(
                            "chevron-down",
                            size=14,
                            class_name="inline-block align-[-2px] ml-1",
                        ),
                        class_name="group-open/details:hidden",
                    ),
                    rx.el.span(
                        "Collapse",
                        rx.icon(
                            "chevron-up",
                            size=14,
                            class_name="inline-block align-[-2px] ml-1",
                        ),
                        class_name="hidden group-open/details:inline",
                    ),
                    class_name=(
                        "list-none cursor-pointer text-center text-sm font-medium "
                        "text-[var(--secondary-11)] hover:text-[var(--secondary-12)] "
                        "pt-12 pb-3 rounded-b-xl "
                        "bg-gradient-to-t from-[var(--secondary-2)] from-55% to-transparent "
                        "group-open/details:pt-3 group-open/details:bg-none "
                        "[&::-webkit-details-marker]:hidden [&::marker]:hidden"
                    ),
                ),
                class_name="group/details absolute bottom-0 left-0 right-0",
            ),
            class_name=(
                "relative max-h-[400px] overflow-hidden mt-4 mb-4 rounded-xl "
                "border border-[var(--secondary-4)] bg-[var(--secondary-2)] "
                "[&_.code-block]:!border-0 "
                "has-[details[open]]:max-h-none"
            ),
        )
    return plain_block


