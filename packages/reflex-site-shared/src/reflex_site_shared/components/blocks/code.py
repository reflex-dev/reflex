def code_block_dark(code: str | rx.Var[str], language: str | rx.Var[str]):
    """Render a dark-demo code block with build-time highlighting for literals.

    Args:
        code: Source code to render.
        language: Shiki language name.

    Returns:
        The code block component.
    """
    if not isinstance(code, str) or not isinstance(language, str):
        return _plain_code_block_dark(code=code, language=language)
    if static_shiki.is_discovery():
        return literal_shiki_code_block(
            code,
            client_component=lambda *_args, **_kwargs: _plain_code_block_dark(
                code=code, language=language
            ),
            language=language,
            class_name="code-block",
            can_copy=True,
        )
    if static_shiki.is_render():
        return rx.box(
            literal_shiki_code_block(
                code,
                language=language,
                class_name="code-block",
                can_copy=True,
            ),
            class_name="relative mb-4",
        )
    return _plain_code_block_dark(code=code, language=language)


def code_block_markdown(*children, **props):
    """Code block markdown.

    Returns:
        The component.
    """
    language = props.get("language", "plain")
    return code_block(code=children[0], language=language)


def code_block_markdown_dark(*children, **props):
    """Code block markdown dark.

    Returns:
        The component.
    """
    language = props.get("language", "plain")
    return code_block_dark(code=children[0], language=language)


def doccmdoutput(
    command: str,
    output: str,
) -> rx.Component:
    """Create a documentation code snippet.

    Args:
        command: The command to display.
        output: The output of the command.

    Returns:
        The styled command and its example output.
    """
    return rx.vstack(
        literal_shiki_code_block(
            command,
            can_copy=True,
            border_radius=styles.DOC_BORDER_RADIUS,
            background="transparent",
            theme="ayu-dark",
            language="bash",
            code_tag_props={
                "style": {
                    "fontFamily": "inherit",
                }
            },
            style=fonts.code,
            font_family="JetBrains Mono",
            width="100%",
        ),
        literal_shiki_code_block(
            output,
            can_copy=False,
            border_radius="12px",
            background="transparent",
            theme="ayu-dark",
            language="log",
            code_tag_props={
                "style": {
                    "fontFamily": "inherit",
                }
            },
            style=fonts.code,
            font_family="JetBrains Mono",
            width="100%",
        ),
        padding_y="1em",
    )
